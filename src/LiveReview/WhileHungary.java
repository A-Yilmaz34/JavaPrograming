package LiveReview;

public class WhileHungary {

    public static void main(String[] args) {

        int bananas = 3;
        int countToFull = 3;

        while (true){

            if (bananas==countToFull){
                break;
            }

            bananas++;
            System.out.println("eating a banana: "+bananas);



        }

        System.out.println("had enough bananas lets get back to study!");










    }}
