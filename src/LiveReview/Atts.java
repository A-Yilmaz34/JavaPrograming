package LiveReview;

public class Atts {

    public String name;
    public String color;
    public int amount;

    public String toString() {
        return "Atts{" +
                "name='" + name + '\'' +
                ", color='" + color + '\'' +
                ", amount=" + amount +
                '}';
    }

    public String asString(){
        return "name : "+name+" color : "+color+" amount : "+amount;
    }





}
