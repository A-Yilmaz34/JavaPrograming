
package day03_EscapeSequences;

public class EscapeSequences {
    public static void main (String[] args){
        System.out.println("Hello, How are you today? \nI am good");//satır atlama
        System.out.println("\tJava is fun"); //paragraf başı
        System.out.println("\\"); //backslash yazmak için 2 tane yapılır.
        System.out.println("My favourite programming language is \"Java\""); // \işareti kensinden sonraki özel işaret de olsa yazdırır
/*birden fazla satırda yorum yazmak için bu kullanılır.

 */
    }

}
