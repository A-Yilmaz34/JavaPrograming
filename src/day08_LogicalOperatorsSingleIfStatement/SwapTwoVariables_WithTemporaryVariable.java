public class SwapTwoVariables_WithTemporaryVariable {
    public static void main(String[] args) {

     int a = 10;
     int b = 15;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

        int c = b; // keeps original value of b
        b = a; //b is 10
        a = c;







    }
}
